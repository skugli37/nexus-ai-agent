// ============================================================================
// Tool Forge Implementation
// ============================================================================

export class ToolForge {
  private zai: Awaited<ReturnType<typeof ZAI.create>> | null = null;
  private toolsPath: string;
  private generatedTools: Map<string, GeneratedTool> = new Map();

  constructor(toolsPath: string = '.nexus/tools') {
    this.toolsPath = toolsPath;
    this.ensureToolsDirectory();
  }

  async initialize(): Promise<void> {
    this.zai = await ZAI.create();
    await this.loadExistingTools();
  }

  /**
   * Forge a new tool from specification
   */
  async forge(spec: ToolSpec): Promise<ForgeResult> {
    if (!this.zai) {
      return { success: false, error: 'ToolForge not initialized' };
    }

    const warnings: string[] = [];
    
    // Validate spec
    const validation = this.validateSpec(spec);
    if (!validation.valid) {
      return { success: false, error: validation.error };
    }

    try {
      // Generate code based on category
      const code = await this.generateToolCode(spec);
      
      // Validate generated code
      const codeValidation = this.validateGeneratedCode(code);
      if (!codeValidation.valid) {
        warnings.push(`Generated code may have issues: ${codeValidation.warning}`);
      }

      // Generate test code
      const testCode = await this.generateTestCode(spec, code);

      // Create tool object
      const tool: GeneratedTool = {
        name: spec.name,
        description: spec.description,
        code,
        testCode,
        filePath: join(this.toolsPath, `${this.toFileName(spec.name)}.tool.ts`),
        createdAt: new Date(),
        spec
      };

      // Write tool to file
      this.writeToolFile(tool);

      // Register tool
      this.generatedTools.set(spec.name, tool);

      // Generate suggestions
      const suggestions = this.generateSuggestions(spec);

      return {
        success: true,
        tool,
        suggestions,
        warnings: warnings.length > 0 ? warnings : undefined
      };

    } catch (error) {
      return {
        success: false,
        error: error instanceof Error ? error.message : 'Unknown error during tool generation'
      };
    }
  }

  /**
   * Generate tool code
   */
  private async generateToolCode(spec: ToolSpec): Promise<string> {
    const category = spec.category || 'utility';
    const template = TOOL_TEMPLATES[category] || TOOL_TEMPLATES.utility;

    // Prepare template variables
    const functionName = this.toCamelCase(spec.name);
    const interfaceName = this.toPascalCase(spec.name);

    // Generate input fields
    const inputFields = Object.entries(spec.inputSchema)
      .map(([name, param]) => {
        const required = param.required ? '' : '?';
        const type = this.mapType(param.type);
        const defaultValue = param.default !== undefined ? ` = ${JSON.stringify(param.default)}` : '';
        return `  ${name}${required}: ${type}${defaultValue}; // ${param.description}`;
      })
      .join('\n');

    // Generate output fields
    const outputFields = spec.outputSchema
      ? Object.entries(spec.outputSchema)
          .map(([name, desc]) => `  ${name}: unknown; // ${desc}`)
          .join('\n')
      : '  success: boolean;\n  result?: unknown;\n  error?: string;';

    // Generate input validation
    const inputValidation = Object.entries(spec.inputSchema)
      .filter(([_, param]) => param.required)
      .map(([name, param]) => {
        if (param.enum) {
          return `  if (!input.${name} || !${JSON.stringify(param.enum)}.includes(input.${name})) {
    throw new Error(\`${name} must be one of: ${param.enum.join(', ')}\`);
  }`;
        }
        return `  if (input.${name} === undefined || input.${name} === null) {
    throw new Error(\`${name} is required\`);
  }`;
      })
      .join('\n');

    // Generate implementation based on category
    const implementation = await this.generateImplementation(spec);

    // Fill template
    let code = template
      .replace(/{name}/g, spec.name)
      .replace(/{description}/g, spec.description)
      .replace(/{interfaceName}/g, interfaceName)
      .replace(/{functionName}/g, functionName)
      .replace(/{inputFields}/g, inputFields)
      .replace(/{outputFields}/g, outputFields)
      .replace(/{inputValidation}/g, inputValidation || '// No validation required')
      .replace(/{implementation}/g, implementation)
      .replace(/{generationPrompt}/g, spec.description);

    // Clean up empty blocks
    code = code.replace(/{\s*}\s*catch/g, '} catch');

    return code;
  }

  /**
   * Generate implementation code
   */
  private async generateImplementation(spec: ToolSpec): Promise<string> {
    const category = spec.category || 'utility';

    switch (category) {
      case 'api':
        return `  // API call implementation
  const response = await fetch(input.url || '', {
    method: input.method || 'GET',
    headers: input.headers || {},
    body: input.body ? JSON.stringify(input.body) : undefined
  });
  
  const data = await response.json();
  
  return {
    success: response.ok,
    data,
    status: response.status
  };`;

      case 'data':
        return `  // Data processing implementation
  const result = input.data;
  
  // Process the data based on input parameters
  if (Array.isArray(result)) {
    // Handle array processing
    return {
      success: true,
      result: result.map(item => item),
      count: result.length
    };
  }
  
  return {
    success: true,
    result
  };`;

      case 'analysis':
        return `  // Analysis implementation
  const text = input.text || '';
  const words = text.split(/\\s+/).filter(w => w.length > 0);
  
  const analysis = {
    wordCount: words.length,
    charCount: text.length,
    avgWordLength: words.reduce((sum, w) => sum + w.length, 0) / Math.max(1, words.length),
    sentences: text.split(/[.!?]+/).filter(s => s.trim().length > 0).length
  };
  
  return {
    success: true,
    analysis
  };`;

      case 'generation':
        return `  // Generation handled by template (uses ZAI)`;

      default:
        return `  // Utility implementation
  return {
    success: true,
    result: input
  };`;
    }
  }

  /**
   * Generate test code
   */
  private async generateTestCode(spec: ToolSpec, toolCode: string): Promise<string> {
    const functionName = this.toCamelCase(spec.name);
    const interfaceName = this.toPascalCase(spec.name);

    // Generate test cases from examples
    const testCases = spec.examples?.map((ex, i) => `
  test('example ${i + 1}: ${ex.description || 'basic test'}', async () => {
    const input = ${JSON.stringify(ex.input, null, 4)};
    const result = await ${functionName}(input);
    
    expect(result.success).toBe(true);
    expect(result).toBeDefined();
  });`).join('\n') || `
  test('basic functionality', async () => {
    const input: ${interfaceName}Input = {
      // Add test input here
    };
    
    const result = await ${functionName}(input);
    
    expect(result).toBeDefined();
    expect(result.success).toBeDefined();
  });`;

    return `/**
 * Tests for ${spec.name}
 * Auto-generated by NEXUS Tool Forge
 */

import { describe, test, expect } from 'bun:test';
import ${functionName}, { ${interfaceName}Input, ${interfaceName}Output } from './${this.toFileName(spec.name)}.tool';

describe('${spec.name}', () => {
${testCases}

  test('handles invalid input gracefully', async () => {
    const result = await ${functionName}({} as any);
    
    expect(result).toBeDefined();
  });
});
`;
  }

  /**
   * Validate tool specification
   */
  private validateSpec(spec: ToolSpec): { valid: boolean; error?: string } {
    if (!spec.name) {
      return { valid: false, error: 'Tool name is required' };
    }

    if (!/^[a-zA-Z][a-zA-Z0-9_-]*$/.test(spec.name)) {
      return { valid: false, error: 'Tool name must start with a letter and contain only letters, numbers, underscores, or hyphens' };
    }

    if (!spec.description) {
      return { valid: false, error: 'Tool description is required' };
    }

    if (!spec.inputSchema || Object.keys(spec.inputSchema).length === 0) {
      return { valid: false, error: 'Tool must have at least one input parameter' };
    }

    return { valid: true };
  }

  /**
   * Validate generated code
   */
  private validateGeneratedCode(code: string): { valid: boolean; warning?: string } {
    // Check for common issues
    if (code.includes('undefined')) {
      return { valid: true, warning: 'Code contains undefined references' };
    }

    if (!code.includes('export')) {
      return { valid: false, warning: 'Code does not export anything' };
    }

    if (code.includes('eval(') || code.includes('Function(')) {
      return { valid: false, warning: 'Code contains potentially unsafe constructs' };
    }

    return { valid: true };
  }

  /**
   * Generate suggestions for tool improvement
   */
  private generateSuggestions(spec: ToolSpec): string[] {
    const suggestions: string[] = [];

    if (!spec.examples || spec.examples.length === 0) {
      suggestions.push('Consider adding examples to improve tool documentation and testing');
    }

    if (!spec.outputSchema) {
      suggestions.push('Consider defining an output schema for better type safety');
    }

    if (Object.keys(spec.inputSchema).length > 5) {
      suggestions.push('Consider grouping related parameters into objects for better organization');
    }

    if (!spec.category) {
      suggestions.push('Consider specifying a category for better organization');
    }

    return suggestions;
  }

  /**
   * Load existing tools from disk
   */
  private async loadExistingTools(): Promise<void> {
    if (!existsSync(this.toolsPath)) {
      return;
    }

    const files = readdirSync(this.toolsPath);
    
    for (const file of files) {
      if (file.endsWith('.tool.ts')) {
        try {
          const filePath = join(this.toolsPath, file);
          const content = readFileSync(filePath, 'utf-8');
          
          // Extract tool info from file
          const nameMatch = content.match(/export async function (\w+)/);
          const descMatch = content.match(/\* (\w+) - (.+)/);
          
          if (nameMatch) {
            const name = nameMatch[1].replace(/([A-Z])/g, '-$1').toLowerCase().replace(/^-/, '');
            
            this.generatedTools.set(name, {
              name,
              description: descMatch?.[2] || '',
              code: content,
              testCode: '',
              filePath,
              createdAt: new Date(),
              spec: {
                name,
                description: descMatch?.[2] || '',
                inputSchema: {}
              }
            });
          }
        } catch (error) {
          console.error(`Failed to load tool ${file}:`, error);
        }
      }
    }
  }

  /**
   * Write tool file to disk
   */
  private writeToolFile(tool: GeneratedTool): void {
    const content = `/**
 * ${tool.name} - ${tool.description}
 * 
 * Auto-generated by NEXUS Tool Forge
 * Generated at: ${tool.createdAt.toISOString()}
 * 
 * @module tools/${tool.name}
 */

${tool.code}

// Tool metadata
export const metadata = {
  name: '${tool.name}',
  description: \`${tool.description}\`,
  createdAt: '${tool.createdAt.toISOString()}',
  generatedBy: 'NEXUS Tool Forge'
};
`;

    writeFileSync(tool.filePath, content);
  }

  /**
   * Delete a tool
   */
  async deleteTool(name: string): Promise<boolean> {
    const tool = this.generatedTools.get(name);
    if (!tool) return false;

    try {
      unlinkSync(tool.filePath);
      this.generatedTools.delete(name);
      return true;
    } catch {
      return false;
    }
  }

  /**
   * Get all generated tools
   */
  getGeneratedTools(): GeneratedTool[] {
    return Array.from(this.generatedTools.values());
  }

  /**
   * Get tool by name
   */
  getTool(name: string): GeneratedTool | undefined {
    return this.generatedTools.get(name);
  }

  /**
   * Check if tool exists
   */
  hasTool(name: string): boolean {
    return this.generatedTools.has(name);
  }

  // Helper methods
  private toFileName(name: string): string {
    return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
  }

  private toCamelCase(name: string): string {
    return name
      .toLowerCase()
      .replace(/[-_\s]+(.)/g, (_, c) => c.toUpperCase())
      .replace(/[^a-zA-Z0-9]/g, '');
  }

  private toPascalCase(name: string): string {
    const camel = this.toCamelCase(name);
    return camel.charAt(0).toUpperCase() + camel.slice(1);
  }

  private mapType(type: string): string {
    const typeMap: Record<string, string> = {
      string: 'string',
      number: 'number',
      boolean: 'boolean',
      object: 'Record<string, unknown>',
      array: 'unknown[]'
    };
    return typeMap[type] || 'unknown';
  }

  private ensureToolsDirectory(): void {
    if (!existsSync(this.toolsPath)) {
      mkdirSync(this.toolsPath, { recursive: true });
    }
  }
}

export default ToolForge;
